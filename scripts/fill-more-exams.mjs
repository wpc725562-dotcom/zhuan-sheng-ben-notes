  // 计算机
  write('历年真题/计算机程序设计/2023.md', CS_2023)
  for (const y of [2021, 2022]) {
    const raw = loadCsTxt(y)
    if (raw) {
      write(
        `历年真题/计算机程序设计/${y}.md`,
        csFromExtract(y, raw, y === 2022 ? CS_2022_HINT : ''),
      )
    }
  }
  // 2024 已有详解，不覆盖；但若抽取文本可作附录存资料
  write('历年真题/计算机程序设计/_索引.md', CS_INDEX)

  write('历年真题/总索引.md', TOTAL_INDEX)

  // 追加资料说明
  const src = path.join(ROOT, '历年真题/00-资料来源与使用说明.md')
  if (fs.existsSync(src)) {
    let t = fs.readFileSync(src, 'utf8')
    if (!t.includes('历年真题大批补全')) {
      t = t.trimEnd() + SOURCE_UPDATE + '\n'
      write('历年真题/00-资料来源与使用说明.md', t)
    }
  }

